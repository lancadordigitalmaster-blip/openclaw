## FILTRO MENTAL — ANTES DE CRIAR QUALQUER COISA

1. PARA QUEM EXATAMENTE?
   Avatar genérico = copy genérico = resultado mediano.
   Avatar real: situação atual, o que já tentou, linguagem exata que usa.
   Se não tiver: pergunte antes de escrever uma linha.

2. QUAL É O NÍVEL DE CONSCIÊNCIA? (Schwartz)
   INCONSCIENTE     → abre com DOR, nunca com produto
   PROBLEMA         → posiciona a CATEGORIA antes da solução
   SOLUÇÃO          → mostra DIFERENCIAL vs outras abordagens
   PRODUTO          → argumento + prova + oferta + urgência
   MAIS CONSCIENTE  → remove a ÚLTIMA objeção + urgência real
   Nível errado = copy perfeito que não converte.

3. O HOOK ESTÁ GANHANDO ATENÇÃO OU OCUPANDO ESPAÇO?
   80% do resultado está nos primeiros 3 segundos ou na primeira linha.

4. ESPECIFICIDADE OU GENERALIDADE?
   "Emagreça rápido" → invisível.
   "Perdi 11kg em 47 dias sem cortar carboidrato" → crível.
   Dado real > afirmação. Número > adjetivo. Transformação > genérico.

5. A OBJEÇÃO PRINCIPAL ESTÁ ENDEREÇADA?
   As 5 universais: dinheiro / tempo / "não funciona pra mim" / confiança / momento certo.

## CLIENTE TRAVADO — PROCESSO DE DESBLOQUEIO

ETAPA 1 — Avatar real: dor antes do produto / o que já tentou / desejo real / linguagem exata
