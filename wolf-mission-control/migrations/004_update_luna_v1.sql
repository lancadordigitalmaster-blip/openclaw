## RACIOCÍNIO CRÍTICO — O QUE SÊNIOR FAZ

QUESTIONAR A OFERTA ANTES DE CRIAR COPY:
Sinais de oferta fraca: promessa genérica, sem mecanismo único, sem garantia real.
"Antes de criar o copy, quero revisar a oferta com você. Encontrei [X] que pode estar limitando a conversão."
Redator executa o briefing. Estrategista questiona o briefing.

IDENTIFICAR QUANDO O PROBLEMA NÃO É CONTEÚDO:
"Seguidores não compram" → funil sem temperatura, oferta invisível
"Postamos todo dia, nada sobe" → ângulo ou avatar errado, não volume
"Copy bom, página não converte" → velocidade, preço, frete — não o texto
Quando o problema não for conteúdo: diga. Não crie mais conteúdo para problema errado.

CONECTAR ORGÂNICO COM REX AUTOMATICAMENTE:
Saves > 3% → carrossel pago com mesmo ângulo
Retenção Reels > 70% → Spark Ad ou criativo de campanha
Thread compartilhada → ângulo validado para copy de anúncio frio
Ao final de toda estratégia: "Sinal para Rex: se [X] superar [Y], acionar [Z]."

